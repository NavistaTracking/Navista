import React, { useState } from 'react';
import { useNavigate, Navigate } from 'react-router-dom';
import { toast, ToastOptions } from 'react-toastify';
import { useAuth } from '../contexts/AuthContext';

const Login: React.FC = () => {
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [isLoading, setIsLoading] = useState(false);
  const { login, user, error, logout } = useAuth();
  const navigate = useNavigate();

  const toastConfig: ToastOptions = {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
  };

  // Only redirect if user is logged in AND has admin role
  if (user && user.role === 'admin') {
    return <Navigate to="/administration_and_development" replace />;
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (isLoading) return;

    try {
      setIsLoading(true);
      await login(email, password);
      
      // Check if the logged-in user has admin role
      const currentUser = await new Promise(resolve => {
        // Small timeout to ensure auth state is updated
        setTimeout(() => resolve(user), 100);
      });
      
      if (currentUser && (currentUser as any).role === 'admin') {
        toast.success('Login successful', toastConfig);
        navigate('/administration_and_development', { replace: true });
      } else {
        toast.error('Access denied. Admin privileges required.', toastConfig);
        // Logout the user if they don't have admin role
        await logout();
      }
    } catch (error) {
      toast.error(error instanceof Error ? error.message : 'An error occurred during login', toastConfig);
    } finally {
      setIsLoading(false);
    }
  };

  return (
    <div className="min-h-screen flex items-center justify-center bg-gray-50">
      <div className="max-w-md w-full space-y-8 p-8 bg-white rounded-lg shadow-lg">
        <div>
          <h2 className="mt-6 text-center text-3xl font-extrabold text-[#4a2a1f]">
            Admin Login
          </h2>
          <p className="mt-2 text-center text-sm text-gray-600">
            Please sign in to access the admin dashboard
          </p>
        </div>
        <form className="mt-8 space-y-6" onSubmit={handleSubmit}>
          <div className="rounded-md shadow-sm -space-y-px">
            <div>
              <label htmlFor="email" className="sr-only">
                Email address
              </label>
              <input
                id="email"
                name="email"
                type="email"
                autoComplete="email"
                required
                value={email}
                onChange={(e) => setEmail(e.target.value)}
                className="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-t-md focus:outline-none focus:ring-[#4a2a1f] focus:border-[#4a2a1f] focus:z-10 sm:text-sm"
                placeholder="Email address"
              />
            </div>
            <div>
              <label htmlFor="password" className="sr-only">
                Password
              </label>
              <input
                id="password"
                name="password"
                type="password"
                autoComplete="current-password"
                required
                value={password}
                onChange={(e) => setPassword(e.target.value)}
                className="appearance-none rounded-none relative block w-full px-3 py-2 border border-gray-300 placeholder-gray-500 text-gray-900 rounded-b-md focus:outline-none focus:ring-[#4a2a1f] focus:border-[#4a2a1f] focus:z-10 sm:text-sm"
                placeholder="Password"
              />
            </div>
          </div>

          <div>
            <button
              type="submit"
              disabled={isLoading}
              className={`group relative w-full flex justify-center py-2 px-4 border border-transparent text-sm font-medium rounded-md text-white bg-[#4a2a1f] hover:bg-[#3a1f17] focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-[#4a2a1f] ${
                isLoading ? 'opacity-50 cursor-not-allowed' : ''
              }`}
            >
              {isLoading ? 'Signing in...' : 'Sign in'}
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default Login; 